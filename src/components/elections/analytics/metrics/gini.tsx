import { Scale } from 'lucide-react';

import { pluralize } from '@/lib/utils/common';
import type { MetricBuilder } from '@/types/metrics';

export const buildGiniMetric: MetricBuilder = ({
  metrics,
  totalBallots,
  choiceCount,
  decryptionDone,
}) => {
  if (!decryptionDone || choiceCount < 2 || metrics.gini === null) return null;

  const g = metrics.gini * 100;
  const n = choiceCount;

  // ── Edge cases ───────────────────────────────────────────────────────────────
  // Gini = 0:   perfect equality — every choice has the exact same share
  // Gini = 100: maximum inequality — one choice took everything

  const isExactZero = g < 0.05;
  const isExactMax = g > 99.95;
  const isVeryHigh = g > 65;
  const isHigh = g > 45;
  const isMid = g >= 20 && g <= 45;

  // ── Card-level interpretation ────────────────────────────────────────────────

  let interpretation: string;

  if (isExactZero) {
    interpretation = `Нульова нерівність — кожен із ${pluralize(n, ['варіант', 'варіанти', 'варіантів'])} отримав рівну частку`;
  } else if (isExactMax) {
    if (totalBallots === 1) {
      interpretation = `Єдиний голос — максимальна нерівність за визначенням`;
    } else {
      interpretation = `Максимальна нерівність — один варіант зібрав усі ${pluralize(totalBallots, ['голос', 'голоси', 'голосів'])}`;
    }
  } else if (isVeryHigh) {
    interpretation = `Різка нерівність — голоси критично сконцентровані в кількох варіантах`;
  } else if (isHigh) {
    interpretation = `Висока нерівність між варіантами — переможець суттєво попереду`;
  } else if (isMid) {
    interpretation = `Помірна нерівність: є лідер, але конкуренти не відстають катастрофічно`;
  } else {
    interpretation = `Відносно рівний розподіл — жоден варіант не домінує безперечно`;
  }

  // ── Detailed insight (modal) ─────────────────────────────────────────────────

  let insight: string;

  if (isExactZero) {
    if (totalBallots < n) {
      // Can't have perfect equality if there are fewer ballots than choices
      // (this edge case shouldn't happen in practice, but let's guard it)
      insight =
        `Коефіцієнт Джині 0% — ідеально рівний розподіл при наявних ${pluralize(totalBallots, ['бюлетені', 'бюлетенях', 'бюлетенях'])}. ` +
        `Зважаючи на малу вибірку, цей результат може бути нестійким — ` +
        `додаткові голоси змінять картину.`;
    } else if (totalBallots % n === 0) {
      insight =
        `Джині 0% — ідеально рівний розподіл: кожен із ${pluralize(n, ['варіанту', 'варіантів', 'варіантів'])} отримав рівно ${totalBallots / n} ${pluralize(totalBallots / n, ['голос', 'голоси', 'голосів'])}. ` +
        `Це математично точний результат, можливий оскільки ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} ділиться на ${n} без залишку. ` +
        `У конкурентному голосуванні подібний баланс — надзвичайна рідкість.`;
    } else {
      insight =
        `Коефіцієнт Джині 0% при ${pluralize(totalBallots, ['бюлетені', 'бюлетенях', 'бюлетенях'])} — ` +
        `виняткова рівність між усіма ${pluralize(n, ['варіантом', 'варіантами', 'варіантами'])}. ` +
        `Жоден не отримав більше за інших. Переможця в такому розподілі немає.`;
    }
  } else if (isExactMax) {
    if (totalBallots === 1) {
      insight =
        `Один бюлетень за одним варіантом — Джині 100% тут є математичною константою, а не аналітичним висновком. ` +
        `Нерівність завжди буде максимальною, поки кількість голосів менша за кількість варіантів.`;
    } else {
      insight =
        `Джині 100% — абсолютна монополія: один варіант зібрав усі ${pluralize(totalBallots, ['голос', 'голоси', 'голосів'])}, ` +
        `решта ${pluralize(n - 1, ['варіант', 'варіанти', 'варіантів'])} не отримала жодного. ` +
        `Це граничний випадок нерівності — реального вибору серед учасників не існувало.`;
    }
  } else if (isVeryHigh) {
    insight =
      `Коефіцієнт Джині ${g.toFixed(1)}% — рівень нерівності, при якому лідер майже монополізував результат. ` +
      `Переважна більшість голосів зосереджена у одного-двох варіантів, ` +
      `а решта ${n > 2 ? pluralize(n - 2, ['варіант', 'варіанти', 'варіантів']) : 'варіантів'} розподілили між собою незначний залишок. ` +
      `Перевага переможця не просто статистична — вона системна.`;
  } else if (isHigh) {
    insight =
      `${g.toFixed(1)}% за Джині — нерівний розподіл з явним лідером. ` +
      `Технічно всі ${pluralize(n, ['варіант', 'варіанти', 'варіантів'])} беруть участь, але частки дуже різняться. ` +
      `Це класичний «довгий хвіст»: один-два піки і різке падіння до решти варіантів.`;
  } else if (isMid) {
    if (g > 35) {
      insight =
        `Джині ${g.toFixed(1)}% вказує на помітний розрив між лідером і конкурентами, але ситуація далека від монополії. ` +
        `Другий і третій варіанти набирають реальні частки, хоча й не можуть зрівнятись з першим. ` +
        `Результат конкурентний, але визначений.`;
    } else {
      insight =
        `${g.toFixed(1)}% — помірна нерівність: є чіткий переможець, але різниця між першим та наступними місцями не надто велика. ` +
        `Голосування відображало реальну диверсифікацію думок, а не консолідацію навколо одного.`;
    }
  } else {
    insight =
      `Джині лише ${g.toFixed(1)}% — один з найрівніших можливих розподілів при ${pluralize(n, ['варіанті', 'варіантах', 'варіантах'])}. ` +
      `Переможець є, але його перевага мінімальна. ` +
      `${totalBallots < 20 ? `При ${pluralize(totalBallots, ['бюлетені', 'бюлетенях', 'бюлетенях'])} навіть 1–2 додаткові голоси на будь-який варіант суттєво змінили б підсумок. ` : 'Будь-яка перерозкладка кількох голосів могла б змінити підсумок. '}` +
      `Такий результат вимагає особливо уважного прочитання.`;
  }

  return {
    id: 'gini',
    icon: <Scale className="h-4 w-4" />,
    label: 'Нерівність розподілу',
    value: `${g.toFixed(1)}%`,
    color: isExactMax || isVeryHigh ? 'error' : isHigh ? 'warning' : isMid ? 'blue' : 'success',
    interpretation,
    description:
      'Коефіцієнт Джині адаптований для розподілу голосів. ' +
      'Запозичений з економічної статистики нерівності доходів. ' +
      '0% — ідеально рівний розподіл між усіма варіантами, 100% — один варіант отримав усе.',
    insight,
    scale: {
      min: 0,
      max: 100,
      current: g,
      gradientFrom: '#10b981',
      gradientTo: '#dc2626',
      labels: ['Рівно', 'Сконцентровано'],
    },
  };
};
