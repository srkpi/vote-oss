import { Activity } from 'lucide-react';

import { pluralize } from '@/lib/utils/common';
import type { MetricBuilder } from '@/types/metrics';

export const buildPeakMetric: MetricBuilder = ({ metrics, totalBallots }) => {
  if (totalBallots < 1 || metrics.peakHourConcentration === null) return null;

  const pct = metrics.peakHourConcentration;
  // peakHourLabel is null when multiple buckets tie for the maximum count.
  // In that case we must not invent a label — the whole point is that no single
  // period stands out.
  const label = metrics.peakHourLabel; // may be null
  const hasSinglePeak = label !== null;
  const peakTiedCount = metrics.peakTiedCount ?? 0;

  // ── Card-level interpretation (tile) ────────────────────────────────────────

  let interpretation: string;

  if (totalBallots === 1) {
    interpretation = 'Єдиний бюлетень — часовий розподіл не має статистичного значення';
  } else if (!hasSinglePeak) {
    // Multiple periods share the maximum — genuinely even distribution
    if (pct >= 50) {
      // Rare: e.g. exactly 2 buckets each with 50% of votes
      interpretation = `Два рівних піки — активність рівно поділена між ${peakTiedCount} проміжками`;
    } else {
      interpretation = `Рівномірна участь — жоден часовий проміжок не виділяється`;
    }
  } else if (pct === 100) {
    interpretation = `Усі ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} надійшли в одному часовому проміжку`;
  } else if (pct >= 90) {
    interpretation = `Практично весь потік голосів — ${pct.toFixed(1)}% — зосереджений у ${label}`;
  } else if (pct >= 75) {
    interpretation = `Різкий одноразовий сплеск у ${label} поглинув три чверті участі`;
  } else if (pct >= 50) {
    interpretation = `Виражений пік у ${label}: більше половини голосів надійшло в одному проміжку`;
  } else if (pct >= 30) {
    interpretation = `Помірний пік у ${label}, решта активності розтягнута рівномірніше`;
  } else if (pct >= 15) {
    interpretation = `Відносно рівна участь — жоден проміжок не концентрував увагу надовго`;
  } else {
    interpretation = `Активність рівномірно розподілена: домінуючого часового піку немає`;
  }

  // ── Detailed insight (modal) ─────────────────────────────────────────────────

  let insight: string;

  if (totalBallots === 1) {
    insight =
      `Голосування містить лише один бюлетень, тому концентрація активності автоматично дорівнює 100% — ` +
      `це не несе жодного аналітичного сенсу. Показник стане значущим, коли накопичиться хоча б кілька голосів.`;
  } else if (!hasSinglePeak) {
    // Tied peaks — genuinely even distribution
    if (peakTiedCount === totalBallots) {
      // Every ballot landed in its own unique bucket — perfectly spread
      insight =
        `Кожен із ${pluralize(totalBallots, ['бюлетеня', 'бюлетенів', 'бюлетенів'])} надійшов у власний окремий часовий проміжок — ` +
        `активність розподілена максимально рівномірно. ` +
        `Жоден момент не виділяється більшою концентрацією, ніж будь-який інший (по ${pct.toFixed(1)}% на кожен проміжок). ` +
        `Це органічний незалежний потік без ознак синхронних нагадувань чи «хвиль».`;
    } else {
      insight =
        `${pluralize(peakTiedCount, ['Проміжок', 'Проміжки', 'Проміжків'])} з ${peakTiedCount} одночасно є максимальними — ` +
        `кожен з них містить по ${pct.toFixed(1)}% бюлетенів. ` +
        `Єдиного домінуючого піку немає: активність розподілена між кількома рівними за інтенсивністю хвилями. ` +
        `Такий паттерн може свідчити про кілька однаково ефективних нагадувань або про рівномірне органічне залучення.`;
    }
  } else if (pct === 100 && totalBallots === 2) {
    insight =
      `Обидва бюлетені надійшли в один часовий проміжок (${label}). ` +
      `При такій малій вибірці говорити про часовий розподіл зарано — будь-які висновки були б передчасними.`;
  } else if (pct === 100) {
    insight =
      `Усі ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} надійшли в межах проміжку ${label} — ` +
      `тимчасового розподілу активності немає взагалі. Голосування відбулось фактично як одноразова подія, ` +
      `а не як розтягнутий у часі процес. Найімовірніша причина: пряме нагадування або спільна зустріч, ` +
      `де всі учасники проголосували синхронно.`;
  } else if (pct >= 90) {
    const rest = (100 - pct).toFixed(1);
    insight =
      `${pct.toFixed(1)}% усіх голосів зосереджені в проміжку ${label}. ` +
      `Решта ${rest}% — це буквально кілька одиничних бюлетенів, що надійшли в інший час і не впливають на загальну картину. ` +
      `Голосування практично не мало часової динаміки: учасники відреагували майже одноразово, ` +
      `найімовірніше, на одне спільне повідомлення або подію.`;
  } else if (pct >= 75) {
    const rest = (100 - pct).toFixed(1);
    insight =
      `Проміжок ${label} зібрав ${pct.toFixed(1)}% голосів — це домінуючий сплеск. ` +
      `Решта ${rest}% розподілились по інших часових відрізках значно рівномірніше. ` +
      `Такий паттерн типовий для голосувань, де основну хвилю спровокувало конкретне нагадування, ` +
      `а частина учасників підключилась пізніше самостійно.`;
  } else if (pct >= 50) {
    const rest = (100 - pct).toFixed(1);
    insight =
      `Більше половини участі — ${pct.toFixed(1)}% — надійшло в ${label}. ` +
      `Це домінуючий пік, але ${rest}% голосів усе ж розподілились в інший час, ` +
      `що свідчить про певну органічну активність поза основною хвилею. ` +
      `Голосування мало виражений центр тяжіння, але не було суто одноразовою подією.`;
  } else if (pct >= 30) {
    insight =
      `Найактивніший проміжок — ${label} — зібрав ${pct.toFixed(1)}% голосів. ` +
      `Це помітний, але не переважний пік: значна частина учасників голосувала незалежно від загального сплеску, ` +
      `у власний зручний час. Голосування поєднувало короткочасний спалах активності з органічним фоновим потоком.`;
  } else if (pct >= 15) {
    insight =
      `Активність відносно рівна: найінтенсивніший проміжок` +
      (label ? ` ${label}` : '') +
      ` отримав лише ${pct.toFixed(1)}% від загального потоку. ` +
      `Голосування не мало яскравого кульмінаційного моменту — учасники підключались поступово, ` +
      `розподіляючи свою активність протягом усього доступного часу.`;
  } else {
    insight =
      `Концентрація ${pct.toFixed(1)}% — один з найнижчих можливих показників. ` +
      `Активність розосереджена практично рівномірно по всьому часовому діапазону голосування. ` +
      `Це характерно для тривалих голосувань без нагадувань, для аудиторій у різних часових зонах ` +
      `або для голосувань, де учасники мали достатньо часу й не відчували терміновості.`;
  }

  return {
    id: 'peak',
    icon: <Activity className="h-4 w-4" />,
    label: 'Пікова активність',
    value: `${pct.toFixed(1)}%`,
    color: pct > 50 ? 'orange' : pct > 25 ? 'warning' : 'success',
    interpretation,
    description:
      'Показує, яка частка від усіх голосів надійшла в найактивніше часове вікно. ' +
      'Для погодинних даних вікно охоплює 3 години, для посохвилинних — 30 хвилин, ' +
      'що дозволяє виявити реальні сплески активності, розмазані по сусідніх проміжках. ' +
      'Висока концентрація вказує на те, що участь стимулювало конкретне нагадування чи подія; ' +
      'низька концентрація характерна для тривалих голосувань з органічним розподіленим потоком.',
    insight,
    scale: {
      min: 0,
      max: 100,
      current: pct,
      gradientFrom: '#16a34a',
      gradientTo: '#f07d00',
      labels: ['Рівномірно', 'Один сплеск'],
    },
  };
};
