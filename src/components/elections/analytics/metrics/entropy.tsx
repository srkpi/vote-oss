import { Scale } from 'lucide-react';

import { pluralize } from '@/lib/utils/common';
import type { MetricBuilder } from '@/types/metrics';

export const buildEntropyMetric: MetricBuilder = ({
  metrics,
  totalBallots,
  choiceCount,
  decryptionDone,
}) => {
  if (!decryptionDone || choiceCount < 2 || metrics.normalizedEntropy === null) return null;

  const ent = metrics.normalizedEntropy * 100;
  const n = choiceCount;

  // ── Edge cases: exact 0% and exact 100% ─────────────────────────────────────

  // 0% entropy: one choice got everything (possible only when one choice has 100%)
  // 100% entropy: all choices are perfectly equal

  const isAbsoluteZero = ent === 0;
  const isPerfect = ent === 100;
  const isVeryHigh = ent > 85;
  const isHigh = ent > 65;
  const isMid = ent >= 35 && ent <= 65;
  const isVeryLow = ent < 15;

  // ── Card-level interpretation ────────────────────────────────────────────────

  let interpretation: string;

  if (isAbsoluteZero) {
    if (totalBallots === 1) {
      interpretation = `Єдиний голос — 100% за одним варіантом, конкуренції немає за визначенням`;
    } else {
      interpretation = `Абсолютна монополія — усі ${pluralize(totalBallots, ['голос', 'голоси', 'голосів'])} за одним варіантом`;
    }
  } else if (isPerfect) {
    interpretation = `Ідеальна рівність — кожен із ${pluralize(n, ['варіант', 'варіанти', 'варіантів'])} отримав однакову частку`;
  } else if (isVeryHigh) {
    interpretation = `Максимальна конкурентність — голоси розподілені між ${pluralize(n, ['варіантом', 'варіантами', 'варіантами'])} майже порівну`;
  } else if (isHigh) {
    interpretation = `Висока конкурентність — явного переможця немає, боротьба реальна`;
  } else if (isMid) {
    interpretation = `Помірна конкурентність: є лідер, але конкуренти не відстають критично`;
  } else if (isVeryLow) {
    if (totalBallots === 1) {
      interpretation = `Єдиний бюлетень визначив повністю однобічний результат`;
    } else {
      interpretation = `Беззаперечний домінант — переважна більшість голосів за одним варіантом`;
    }
  } else {
    interpretation = `Низька конкурентність — один варіант суттєво випереджає інших`;
  }

  // ── Detailed insight (modal) ─────────────────────────────────────────────────

  let insight: string;

  if (isAbsoluteZero) {
    if (totalBallots === 1) {
      insight =
        `Один голос — один варіант. Ентропія 0% тут не означає «монополію» в статистичному сенсі: ` +
        `просто вибірка надто мала, щоб говорити про конкурентність. ` +
        `Показник набуде реального значення, коли надійде більше бюлетенів.`;
    } else {
      insight =
        `Ентропія 0% — абсолютна монополія: усі ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} ` +
        `віддані за один варіант, решта ${pluralize(n - 1, ['варіант', 'варіанти', 'варіантів'])} не отримала жодного голосу. ` +
        `Це не «слабкий лідер» — це повна відсутність конкуренції. ` +
        `Можливі причини: варіанти нерівнозначні за популярністю, учасники заздалегідь домовились, ` +
        `або альтернативи взагалі не сприймались як реальні.`;
    }
  } else if (isPerfect) {
    // Perfect entropy: extremely rare, especially with many choices
    if (n === 2) {
      insight =
        `Ентропія 100% при двох варіантах означає ідеальний розкол 50/50. ` +
        `Учасники розділились рівно навпіл — результат визначить буквально будь-яке додаткове голосування. ` +
        `${totalBallots % 2 === 0 ? `Оскільки ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} є парна кількість, розкол математично точний.` : ''}`;
    } else {
      insight =
        `Ентропія 100% при ${pluralize(n, ['варіанті', 'варіантах', 'варіантах'])} — це математично рідкісна ситуація: ` +
        `кожен варіант отримав абсолютно однакову частку голосів. ` +
        `${totalBallots % n === 0 ? `Це можливо, оскільки ${pluralize(totalBallots, ['бюлетень', 'бюлетені', 'бюлетенів'])} ділиться на ${n} без залишку. ` : ''}` +
        `Думки учасників розподілились абсолютно рівномірно — кожен варіант має рівні права на перемогу.`;
    }
  } else if (isVeryHigh) {
    insight =
      `Ентропія ${ent.toFixed(1)}% означає розподіл, що лише незначно відрізняється від ідеально рівного між ${pluralize(n, ['варіантом', 'варіантами', 'варіантами'])}. ` +
      `Це рідкісна ситуація — або всі варіанти справді рівноцінні в очах аудиторії, ` +
      `або думки учасників принципово розходяться і кожен голосує «за своє». ` +
      `Будь-який варіант може претендувати на перемогу з мінімальним відривом.`;
  } else if (isHigh) {
    insight =
      `Показник ${ent.toFixed(1)}% свідчить про справжню конкуренцію між ${pluralize(n, ['варіантом', 'варіантами', 'варіантами'])}: ` +
      `жоден не може претендувати на беззаперечну підтримку. ` +
      `Різниця між частками є, але жоден кандидат не контролює ситуацію повністю. ` +
      `Результат визначали нюанси — незначна зміна кількох голосів могла б змінити підсумок.`;
  } else if (isMid) {
    if (ent >= 50) {
      insight =
        `Ентропія ${ent.toFixed(1)}% — є помітний лідер, але решта ${pluralize(n - 1, ['варіант', 'варіанти', 'варіантів'])} теж набирає реальні частки. ` +
        `Конкуренція не символічна: перемога здобута, але не нищівна.`;
    } else {
      insight =
        `При ентропії ${ent.toFixed(1)}% розподіл скошений до кількох варіантів. ` +
        `Є виражений лідер або два, а більшість інших варіантів отримала помітно менше. ` +
        `Рівної боротьби між усіма ${pluralize(n, ['варіантом', 'варіантами', 'варіантами'])} не було.`;
    }
  } else if (isVeryLow) {
    insight =
      `Ентропія лише ${ent.toFixed(1)}% — голосування фактично безальтернативне на практиці. ` +
      `Один варіант зібрав переважну частку голосів, ` +
      `решта ${pluralize(n - 1, ['варіант', 'варіанти', 'варіантів'])} отримала лише символічну підтримку. ` +
      `Формально вибір був, але реальної конкуренції не спостерігалось. ` +
      `Причини можуть бути різні: від очевидного фаворита до слабкої обізнаності учасників з альтернативами.`;
  } else {
    insight =
      `${ent.toFixed(1)}% — нижній діапазон конкурентності. Один чи два варіанти забирають основну частку, ` +
      `інші залишаються на периферії. Формально вибір є, але реально конкурує лише один основний претендент.`;
  }

  return {
    id: 'entropy',
    icon: <Scale className="h-4 w-4" />,
    label: 'Конкурентність виборів',
    value: `${ent.toFixed(1)}%`,
    color: isVeryHigh || isHigh || isPerfect ? 'success' : isMid ? 'blue' : 'orange',
    interpretation,
    description:
      'Показник інформаційної ентропії Шеннона, нормалізований до кількості варіантів. ' +
      '100% означає ідеально рівний розподіл голосів між усіма варіантами, ' +
      '0% — абсолютна монополія одного варіанта.',
    insight,
    scale: {
      min: 0,
      max: 100,
      current: ent,
      gradientFrom: '#f07d00',
      gradientTo: '#10b981',
      labels: ['Один лідер', 'Всі рівні'],
    },
  };
};
